package com.sssta.qinbot.model;

public class Friend {
	private String uni;
	private int friendFlag;
	private int infoFlag;
	private String nickName;
	private int categories;
	private String markName;
	private int face;
	private int markNameType;
	public String getUni() {
		return uni;
	}
	public void setUni(String uni) {
		this.uni = uni;
	}
	public int getFriendFlag() {
		return friendFlag;
	}
	public void setFriendFlag(int friendFlag) {
		this.friendFlag = friendFlag;
	}
	public int getInfoFlag() {
		return infoFlag;
	}
	public void setInfoFlag(int infoFlag) {
		this.infoFlag = infoFlag;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nikeName) {
		this.nickName = nikeName;
	}
	public int getCategories() {
		return categories;
	}
	public void setCategories(int categories) {
		this.categories = categories;
	}
	public String getMarkName() {
		return markName;
	}
	public void setMarkName(String markName) {
		this.markName = markName;
	}
	public int getFace() {
		return face;
	}
	public void setFace(int face) {
		this.face = face;
	}
	public int getMarkNameType() {
		return markNameType;
	}
	public void setMarkNameType(int markNameType) {
		this.markNameType = markNameType;
	}
	
	
}
